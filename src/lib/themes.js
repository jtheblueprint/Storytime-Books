export function applyTheme(themeId, mode, root) {
  const theme = themes[themeId];
  if (!theme) return;
  const colors = theme[mode];
  Object.entries(colors).forEach(([key, value]) => {
    root.style.setProperty(`--${key}`, value);
  });
}
